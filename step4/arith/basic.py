class Basic:
    def add(self, val1, val2):
        result = val1 + val2
        return result

    def subtract(self, val1, val2):
        result = val1 - val2
        return result

    def multiply(self, val1, val2):
        result = val1 * val2
        return result

    def divide(self, val1, val2):
        result = val1 /  val2
        return result
